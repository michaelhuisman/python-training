#!/usr/bin/env python3
def omkeer(s):
    """keert een string om
    parameter:
    s - de om te keren string (VALUE)
    """
    return s[::-1]
def makeupper(s):
    """Maakt van een string een uppercase string
    parameter:
    s - de naar hoofdletters te converteren string (VALUE)
    """
    return s.upper()
